inline float &Eigen::Matrix(int row, int col)
{
	return(m_mat[row][col]);
}

/***************************************************************************/
/***************************************************************************/
inline float Eigen::GetEigenvalue(int i) const
{
	return(m_diag[i]);
}

/***************************************************************************/
/***************************************************************************/
inline float Eigen::GetEigenvector(int row, int col) const
{
	return(m_mat[row][col]);
}

/***************************************************************************/
/***************************************************************************/
inline float *Eigen::GetEigenvalue(void)
{
	return(m_diag);
}

/***************************************************************************/
/***************************************************************************/
inline float **Eigen::GetEigenvector(void)
{
	return(m_mat);
}

