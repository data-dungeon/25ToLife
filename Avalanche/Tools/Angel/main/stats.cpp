Stats *g_stats;

//***************************************************************************
//***************************************************************************
void Stats::Clear(void)
{
	inputFile = 0;
	outputFile = 0;
	totalInstances = 0;

	currentInstance.Cpy("");
	onInstance = 0;
	done = false;

	status.Cpy("");
	percentageDone = 0.0f;
}

//***************************************************************************
//***************************************************************************
void Stats::SetStatus(char *newStatus)
{ 
	status.Cat(newStatus); 
	status.Cat("\r\n"); 
}

//***************************************************************************
//***************************************************************************
void Stats::ClearStatus(void)
{ 
	status.Cpy(""); 
}
