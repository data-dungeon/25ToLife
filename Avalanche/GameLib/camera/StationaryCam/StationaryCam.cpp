StationaryWTVC::StationaryWTVC()
{
	m_localTarget.Clear();
	SetTarget(&m_localTarget);
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
void StationaryWTVC::Setup(const Vector3 &eye, const Vector3 &target)
{
	m_eye = eye;
	m_localTarget = target;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
void StationaryWTVC::SetLocalTarget(const Vector3 &target)
{ 
	m_localTarget = target; 
	SetTarget(&m_localTarget);  
}

