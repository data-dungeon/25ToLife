//////////////////////////////////////////////////////////////////////////
// A simple default viewport controller
//////////////////////////////////////////////////////////////////////////
#include "camera/CameraPCH.h"

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////
DefaultViewportController::DefaultViewportController()
{
	Setup(0.0f, 0.0f, 1.0f, 1.0f);
}

//////////////////////////////////////////////////////////////////////////
// The upper left hand corner
//////////////////////////////////////////////////////////////////////////
float DefaultViewportController::XOffset()
{
	return m_x;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////
float DefaultViewportController::YOffset()
{
	return m_y;
}

//////////////////////////////////////////////////////////////////////////
// The lower right hand corner
//////////////////////////////////////////////////////////////////////////
float DefaultViewportController::Width()
{
	return m_width;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////
float DefaultViewportController::Height()
{
	return m_height;
}

//////////////////////////////////////////////////////////////////////////
// Setup the viewport
//////////////////////////////////////////////////////////////////////////
void DefaultViewportController::Setup(
float x,
float y,
float width,
float height)
{
	m_x = x;
	m_y = y;
	m_width = width;
	m_height = height;
}

