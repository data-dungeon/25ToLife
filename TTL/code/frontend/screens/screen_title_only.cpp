///////////////////////////////////////////////////////////////////////////////////////////////////
/* virtual */
void ScreenTitleOnly::OnScreenUpdate( void )
{
	ScreenBaseClass::OnScreenUpdate();
}

void ScreenTitleOnly::SetTitle( const char* title )
{
	if( !m_pScreen ) return;
	if( GetTextSprite( ID_MY_TITLE ) )
	{
		GetTextSprite( ID_MY_TITLE )->SetText( title );
	}
}